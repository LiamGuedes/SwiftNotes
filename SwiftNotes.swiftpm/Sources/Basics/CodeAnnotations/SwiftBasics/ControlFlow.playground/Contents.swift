// MARK: Boolean  Operator
/*  Boolean operators in swift is used to compare values. It can be equality (==), different (!=), greater than (>) and less than (<)
*/

let jamesAge: Int = 25
let maryAge: Int = 23

print(jamesAge == maryAge)
print(jamesAge != maryAge)
print(jamesAge > maryAge)
print(jamesAge < maryAge)
print(jamesAge >= maryAge)
